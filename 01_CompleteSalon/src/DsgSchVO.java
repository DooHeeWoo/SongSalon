
public class DsgSchVO {
private String dsrsch_code;
private String dsrsch_dcode;
private String dsrsch_time;
private String dsrsch_date;
public DsgSchVO(String dsrsch_code, String dsrsch_dcode, String dsrsch_time, String dsrsch_date) {

	this.dsrsch_code = dsrsch_code;
	this.dsrsch_dcode = dsrsch_dcode;
	this.dsrsch_time = dsrsch_time;
	this.dsrsch_date = dsrsch_date;
}
public String getDsrsch_code() {
	return dsrsch_code;
}
public String getDsrsch_dcode() {
	return dsrsch_dcode;
}
public String getDsrsch_time() {
	return dsrsch_time;
}
public String getDsrsch_date() {
	return dsrsch_date;
}

	
	
	

}
